/*
    项目的开发是一个不断迭代的过程，开发过程中程序员需要不断的对代码进行编写和更正。这就带来很多的问题。
        首先，开发中代码会存在多个版本，我们如何将代码在多个版本间进行切换？
        第二，代码上线后，如何在不影响现行开发工作的情况下对代码进行维护？
        第三，开发时某段代码被多人修改时，如何处理代码的冲突问题？
        除此之外，还有存储效率、远程仓库等问题。
    git是一个免费开源的版本控制系统，它被设计用来快速高效地管理项目开发的源码。
        通过git可以跟踪代码的状态，也可以在修改代码后对代码状态进行存储，
            还可以在需要时将已经修改过的代码恢复到之前存储的状态。
        更强大的是使用git管理代码时，可以创建代码分支（branch），
            代码分支相当于一段独立的代码记录，我们可以在分支上对代码进行任意的修改，
                而这个修改只会影响当前分支，不会对其他分支产生影响。
            同时，可以对分支进行合并，合并后一个分支的修改便可在另一分支上生效。
        总之，git是当今最优秀的版本控制工具！
*/ 